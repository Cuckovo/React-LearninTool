/**
 * 构建首条 system message 的节点上下文内容。
 *
 * @param nodeId 节点ID（如 "kn-conc-1-1-1"），用于 DB 指令生成
 * @param nodeLabel 节点标签（如 "1.1.1 映射"）
 * @param standardDefinition 标准定义（教材原文）
 * @param masteryStatus 当前掌握状态
 * @returns 节点上下文字符串，作为首条 system message 的一部分
 */
export function buildKnowledgeContext(
  nodeId: string,
  nodeLabel: string,
  standardDefinition: string | null,
  masteryStatus: MasteryStatus,
): string {
  const statusLabel: Record<MasteryStatus, string> = {
    not_started: '未开始学习',
    learning: '学习中',
    passed: '已通过考核',
    mastered: '已掌握',
  };

  let context = `## 当前学习节点\n\n**节点ID**：${nodeId}\n**知识点**：${nodeLabel}\n**掌握状态**：${statusLabel[masteryStatus]}\n\n`;

  if (standardDefinition) {
    context += `**教材定义**：\n${standardDefinition}\n\n`;
  }

  context += `请围绕以上知识点展开教学。如果用户尚未提出具体问题，先做一个简要的概念导入（2-3 句话），然后询问用户想从哪个角度开始学习。

## 重要：DB 操作指令
在回复末尾，你可以附加 \`\`\`db 代码块来保存数据。当前节点的 ID 是 \`${nodeId}\`。

### 保存笔记
\`\`\`db
{ "action": "set_user_notes", "nodeId": "${nodeId}", "value": "笔记内容" }
\`\`\`

### 更新掌握状态
\`\`\`db
{ "action": "set_mastery", "nodeId": "${nodeId}", "value": "learning|passed|mastered" }
\`\`\`

当用户理解了一个概念后，请主动保存一条简洁的笔记摘要。

## 考核触发词提醒
仅当用户明确说"考考我/考核/测试/测一下/来几道题/检验一下/出题/做题"时才出题。换题/换简单的/重来等不是考核触发词。`;

  return context;
}
